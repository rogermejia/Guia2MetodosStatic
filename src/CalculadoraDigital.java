
public class CalculadoraDigital extends Calculadora{

	@Override
	public static void suma(Double i, Double j) {
		System.out.println(i + j);
		
	}

	@Override
	public static void resta(Double i, Double j) {
		System.out.println(i - j);
		
	}

	@Override
	public static void multiplicacion(Double i, Double j) {
		System.out.println(i * j);
		
	}

	@Override
	public static void division(Double i, Double j) {
		System.out.println(i / j);
		
	}

	

}
